class I18n:
    def __init__(self, lang='en'):
        self.lang = lang if lang in TRANSLATIONS else 'en'

    def _(self, text, **kwargs):
        """Translate `text` to current locale. Fall back to English if no translation found."""
        tr = TRANSLATIONS.get(self.lang, {}).get(text)
        if tr is None:
            tr = text
        if kwargs:
            try:
                return tr % kwargs
            except (KeyError, TypeError):
                return tr
        return tr


def get_locale():
    """Determine locale: session > cookie > browser > config default."""
    lang = session.get('lang')
    if lang and lang in LANGUAGES:
        return lang
    lang = request.cookies.get('lang')
    if lang and lang in LANGUAGES:
        return lang
    # Accept-Language header
    best = request.accept_languages.best_match(list(LANGUAGES.keys()))
    if best:
        return best
    # Config default (injected by context processor)
    return 'en'


def _(text, **kw):
    """Module-level translate helper — import in blueprints as: from ..i18n import _"""
    return I18n(get_locale())._(text, **kw)


def register_i18n(app):
    """Register i18n context processor and before_request handler."""

    @app.before_request
    def set_locale():
        g.lang = get_locale()
        g.i18n = I18n(g.lang)

    @app.context_processor
    def inject_i18n():
        return {
            '_': g.get('_', lambda t, **kw: I18n(get_locale())._(t, **kw)),
            'current_lang': g.get('lang', 'en'),
            'languages': LANGUAGES,
        }

    # Also add `_` to Jinja2 globals so it works outside request context (e.g., in macros)
    app.jinja_env.globals['_'] = lambda t, **kw: I18n(get_locale())._(t, **kw)
    app.jinja_env.globals['languages'] = LANGUAGES
